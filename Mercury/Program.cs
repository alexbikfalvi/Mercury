using System;
using System.Windows.Forms;
using Microsoft.Win32;
using Mercury.Forms;
using MercuryApi;

namespace Mercury
{
	/// <summary>
	/// The main class of the Mercury console.
	/// </summary>
	static class Program
	{
		/// <summary>
		/// The main entry point for the application.
		/// </summary>
		[STAThread]
		static void Main()
		{
			Application.EnableVisualStyles();
			Application.SetCompatibleTextRenderingDefault(false);

			try
			{
				using (MercuryApi.Mercury mercury = new MercuryApi.Mercury(Registry.CurrentUser, "Software\\Alex Bikfalvi\\Mercury"))
				{
					Application.Run(new FormMain(mercury));
				}
			}
			catch { }
		}
	}
}
